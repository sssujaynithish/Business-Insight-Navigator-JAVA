    private void AddToCart_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddToCart_btnActionPerformed
        // TODO add your handling code here:
        String itemName = txt_ItemName.getText();
        int quantity = Integer.parseInt(txt_NoOfItems.getText());
        double mrp = Double.parseDouble(txt_mrp.getText());
        double total = Double.parseDouble(txt_total_price.getText());

        // Add the item to the cart model
        cartModel.addRow(new Object[]{itemName, quantity, total});

        // Insert the data into the database
        try {
            Connection con = connectionProvider.getCon();
            PreparedStatement pst = con.prepareStatement("INSERT INTO cart (item_name, no_of_items, mrp, total) VALUES (?, ?, ?, ?)");
            pst.setString(1, itemName);
            pst.setInt(2, quantity);
            pst.setDouble(3, mrp);
            pst.setDouble(4, total);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Item added to cart and database successfully.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_AddToCart_btnActionPerformed

