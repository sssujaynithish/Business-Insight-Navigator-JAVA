    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat myf = new SimpleDateFormat("dd-MM-yyyy");
        Calendar cal = Calendar.getInstance();
        txt_currentDate.setText(myf.format(cal.getTime()));
        String name = txt_ItemName.getText();
        String no_of_items = txt_NoOfItems.getText();
        Double price;
        try {
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from products where name='" + name + "'");
            while (rs.next()) {
                txt_mrp.setText(rs.getString("mrp"));
                txt_total_price.setText(String.valueOf(Double.parseDouble(no_of_items) * Double.parseDouble(rs.getString("mrp"))));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

