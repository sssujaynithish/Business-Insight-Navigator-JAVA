    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       Document document = new Document();

    try {
        PdfWriter.getInstance(document, new FileOutputStream("invoice.pdf"));

        document.open();

        // Add invoice title
        Font titleFont = FontFactory.getFont(FontFactory.HELVETICA_BOLD, 18);
        Paragraph title = new Paragraph("Invoice", titleFont);
        title.setAlignment(Paragraph.ALIGN_CENTER);
        document.add(title);

        // Add current date
        Font dateFont = FontFactory.getFont(FontFactory.HELVETICA, 12);
        Paragraph date = new Paragraph("Date: " + txt_currentDate.getText(), dateFont);
        date.setAlignment(Paragraph.ALIGN_RIGHT);
        document.add(date);

        // Create table for item details
        PdfPTable table = new PdfPTable(3); // 3 columns
        table.setWidthPercentage(100);

        // Add table headers
        Font headerFont = FontFactory.getFont(FontFactory.HELVETICA_BOLD, 12);
        table.addCell(new Paragraph("Item Name", headerFont));
        table.addCell(new Paragraph("Quantity", headerFont));
        table.addCell(new Paragraph("Price (₹)", headerFont));

        // Iterate through each row in the cart model and add items to the table
        for (int i = 0; i < cartModel.getRowCount(); i++) {
            String itemName = cartModel.getValueAt(i, 0).toString();
            String quantity = cartModel.getValueAt(i, 1).toString();
            String price = cartModel.getValueAt(i, 2).toString();

            table.addCell(new Paragraph(itemName));
            table.addCell(new Paragraph(quantity));
            table.addCell(new Paragraph(price));
        }

        // Add table to the document
        document.add(table);

        // Add total amount
        double totalAmount = 0;
        for (int i = 0; i < cartModel.getRowCount(); i++) {
            totalAmount += Double.parseDouble(cartModel.getValueAt(i, 2).toString());
        }
        Paragraph total = new Paragraph("Total: ₹" + String.format("%.2f", totalAmount), headerFont);
        total.setAlignment(Paragraph.ALIGN_RIGHT);
        document.add(total);

        System.out.println("Invoice generated successfully.");
    } catch (DocumentException | IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error generating PDF: " + e.getMessage());
    } finally {
        if (document != null) {
            document.close();
        }
    }

    // Optionally, you can clear the cart model after generating the invoice
    cartModel.setRowCount(0);
    }//GEN-LAST:event_jButton2ActionPerformed

