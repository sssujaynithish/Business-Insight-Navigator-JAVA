    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        inventory_table.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                // Check if any row is selected
                if (inventory_table.getSelectedRow() != -1) {
                    // Enable the update button
                    btn_Update.setEnabled(true);
                } else {
                    // Disable the update button if no row is selected
                    btn_Update.setEnabled(false);
                }
            }
        });
        DefaultTableModel model = (DefaultTableModel) inventory_table.getModel();
        Double totalprofit = 0.0;
        try {
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from products");
            while (rs.next()) {
                double profit = Double.parseDouble(rs.getString("profit"));
                totalprofit += profit;
                model.addRow(new Object[]{rs.getString("name"), rs.getString("buyPrice"), rs.getString("no_of_items"), rs.getString("mrp"), rs.getString("expireyDate"), rs.getString("profit")});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        label_totalProfit.setText("Total Profit: " + totalprofit);
        btn_Update.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

