    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // TODO add your handling code here:
        String name = txt_Name.getText();
        String buyPrice = txt_BuyPrice.getText();
        String expireyDate = txt_ExpireyDate.getText();
        String no_of_items = txt_NoOfItems.getText();
        String mrp = txt_SellPrice.getText();
        String profit = String.valueOf((Double.parseDouble(mrp) - Double.parseDouble(buyPrice)) * Integer.parseInt(no_of_items));

        try {
            Connection con = connectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("INSERT INTO products (name, buyPrice, no_of_items, mrp, expireyDate, profit) VALUES (?, ?, ?, ?, ?, ?)");
            ps.setString(1, name);
            ps.setString(2, buyPrice);
            ps.setString(3, no_of_items);
            ps.setString(4, mrp);
            ps.setString(5, expireyDate);
            ps.setString(6, profit);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Product Added Successfully!");
            setVisible(false);
            new InventoryProductPage().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_btn_SaveActionPerformed

    private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpdateActionPerformed
        // TODO add your handling code here:
        String name = txt_Name.getText();
        String buyPrice = txt_BuyPrice.getText();
        String no_of_items = txt_NoOfItems.getText();
        String mrp = txt_SellPrice.getText();
        String expireyDate = txt_ExpireyDate.getText();
        String profit = String.valueOf((Double.parseDouble(mrp) - Double.parseDouble(buyPrice)) * Integer.parseInt(no_of_items));

        int selectedRowIndex = inventory_table.getSelectedRow();
        System.out.println(selectedRowIndex);
        if (selectedRowIndex != -1) {
            DefaultTableModel model = (DefaultTableModel) inventory_table.getModel();
            try {
                Connection con = connectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("UPDATE products SET name=?, buyPrice=?, no_of_items=?, mrp=?, expireyDate=?, profit=? WHERE s_no=?");
                ps.setString(1, name);
                ps.setString(2, buyPrice);
                ps.setString(3, no_of_items);
                ps.setString(4, mrp);
                ps.setString(5, expireyDate);
                ps.setString(6, profit);
                ps.setString(7, String.valueOf(selectedRowIndex + 1));
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Product updated successfully!");

                model.setValueAt(name, selectedRowIndex, 0);
                model.setValueAt(buyPrice, selectedRowIndex, 1);
                model.setValueAt(no_of_items, selectedRowIndex, 2);
                model.setValueAt(mrp, selectedRowIndex, 3);
                model.setValueAt(expireyDate, selectedRowIndex, 4);
                model.setValueAt(profit, selectedRowIndex, 5);
                setVisible(false);
                new InventoryProductPage().setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            txt_Name.setText("");
            txt_BuyPrice.setText("");
            txt_NoOfItems.setText("");
            txt_SellPrice.setText("");
            txt_ExpireyDate.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to update.");
        }

    }//GEN-LAST:event_btn_UpdateActionPerformed

